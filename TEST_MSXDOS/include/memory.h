#ifndef __MEMORY_H__
#define __MEMORY_H__

#include "newTypes.h"



/* =============================================================================
 lee un valor de 1Byte de la RAM
============================================================================= */
byte PEEK(uint address);



/* =============================================================================
 lee un valor de 16bits de la RAM
 OLD: peek_word
============================================================================= */
uint PEEKW(uint address);



/* =============================================================================
 escribe un valor a la RAM 
 by Andrear
============================================================================= */
void POKE(uint address, byte value);



/* =============================================================================
 escribe un valor a la RAM 
 by Andrear
 OLD: poke_word
============================================================================= */
void POKEW(uint address, uint value);



/* =============================================================================
 escribe un conjunto de datos a la RAM
============================================================================= */
void CopyRAM(uint address, char* block, int length);



/* =============================================================================
 llena una area de la RAM con un valor
============================================================================= */
void FillRAM(uint address, int size, byte value);




#endif